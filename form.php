            var n1 = parseInt(p1)
            var n2 = parseInt(p2)
            var n3 = parseInt(p3)
            var n4 = parseInt(p4)
            var n5 = parseInt(p5)
            var n6 = parseInt(p6)

            //operação
            var media = (n1 + n2 + n3 + n4 + n5 + n6) / 6;
            
             if (media <= 1) {
                swal({
                    type: 'error',
                    title: 'Teste de Personalidade',
                    text: 'Sinto muito, acho que nosso curso não vai lhe agradar',
                });
                
            }else if (media <= 2) {
                swal({
                    type: 'warning',
                    title: 'Teste de Personalidade',
                    text: 'Continue procurando, seu curso dos sonhos está por perto',
                });
                
            }else if (media <= 3) {
                swal({
                    type: 'success',
                    title: 'Teste de Personalidade',
                    text: 'Você irá se encaixar em algum aspecto do curso',
                });
                
            }else if (media <= 4) {
                swal({
                    type: 'success',
                    title: 'Teste de Personalidade',
                    text: 'Esse é o curso ideal pra você!',
                });
                
            }
        }

